#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	s_len;
	char	*string;

	if (!s)
		return (NULL);
	i = 0;
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (start + len > s_len)
		len = s_len - start;
	string = malloc((len + 1) * sizeof(char));
	if (!string)
		return (NULL);
	while (i < len && s[start])
		string[i++] = s[start++];
	string[i] = '\0';
	return (string);
}
